@app.route('/sendemail/', methods=["POST"])
def sendemail():
  name = request.form['name']
  subject = request.form['Subject']
  email = request.form['_replyto']
  message = request.form['message']

  your_email = "your_email@gmail.com"
  your_password = "your_app_password"

  msg = EmailMessage()
  msg.set_content(f"Name: {name}\nEmail: {email}\nSubject: {subject}\nMessage:\n{message}")
  msg['Subject'] = f"Portfolio Contact: {subject}"
  msg['From'] = your_email
  msg['To'] = your_email
  try:
    server = smtplib.SMTP("smtp.gmail.com", 587)
    server.starttls()
    server.login(your_email, your_password)
    server.send_message(msg)
    server.quit()
    return "<h4>Email sent successfully!</h4><a href='/'>Go Back</a>"
  except Exception as e:
    return f"<h4>Failed to send email: {e}</h4><a href='/'>Go Back</a>"

